day_of_week = int(input('Day (0-6)?')) 


if day_of_week == 0:
    print ('Sunday')
elif day_of_week == 1:
    print ('Monday') 
elif day_of_week == 2:
    print ('Tuesday')
elif day_of_week == 3: 
    print ('Wednesday')
elif day_of_week == 4: 
    print ('Thursday')
elif day_of_week == 5:
    print ('Friday') 
elif day_of_week == 6:
    print ('Saturday')
else:
    print ('Please enter a postive value between 0 and 6')
        

